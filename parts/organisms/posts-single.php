			<h1 class="posts-single__title title-2">¿Qué es el CBU?</h1>
			<div class="posts-single__content editor">
				<p><strong>Miclik te acerca, en este breve artículo, información respecto al CBU. <br>Qué es y como obtenerlo.</strong></p>
				<p>El CBU es un número compuesto por 22 dígitos con el cual podrás realizar cualquier tipo de transferencia bancaria o depósito de dinero, ya que identifica tu cuenta bancaria. ¡Poder obtenerlo es más fácil de lo que pensas!</p>
				<p><strong>¿Cómo obtengo mi CBU?</strong><br>
				Lo podés consultar desde tu home banking, haciendo click en la opción "Consulta de CBU" o acercándote a cualquier cajero automático. Para ello, tendrás que ingresar en tu cuenta bancaria, seleccionar la opción Consultas y luego Consulta de CBU. Hacer clic acá para conocer más.</p>
